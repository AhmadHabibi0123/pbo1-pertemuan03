package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanjalan=3;
        switch (jalanjalan){
            case 1:
                System.out.println("Anak Lapas");
                break;
            case 2:
                System.out.println("Pacaran ja pang");
                break;
            case 3:
                System.out.println("Jadi Obat Nyamuk");
                break;
            case 4:
                System.out.println("Urang Halus");
                break;
        }
    }
}
